#!/bin/bash

###############################################################################
# 系统模板 - 前端构建脚本
# 用途: 在本地构建 Vue 生产版本
###############################################################################

set -e

echo "========================================="
echo "开始构建前端..."
echo "========================================="

# 进入前端目录
cd "$(dirname "$0")/../../system-vue"

echo "1. 安装依赖..."
npm install

echo "2. 开始构建生产版本..."
export NODE_OPTIONS=--openssl-legacy-provider
npm run build

echo "3. 复制构建产物到部署目录..."
mkdir -p ../deploy/build/frontend
cp -r dist/* ../deploy/build/frontend/

echo "========================================="
echo "前端构建完成！"
echo "构建产物位置: deploy/build/frontend/"
echo "========================================="
